                    <h3>Monuments : </h3>
                    <br>
                        <p class="text">
                        Vers le ixe siècle, les seigneurs musulmans de Madrid édifient une muraille défensive dont il subsiste quelques tronçons comme dans 
                        le parc Mohammed Ier. Ils font aussi creuser des canaux d'irrigation et de transport des eaux (qanats).<br>
                        <br>
                        Entre 1083 et 1085, la ville passe sous contrôle chrétien et voit apparaître ses premières églises. Bon nombre d'entre elles sont 
                        par la suite détruites ou reconstruites dans un style différent, à l'image des églises Saint-André et Saint-Nicolas-de-Bari. Au 
                        xve siècle, la tour et la maison des Luján sont édifiées sur l'actuelle place de la Bourgade (plaza de la Villa).<br>
                        <br>
                        Les armoiries de la ville, qui remontent au xiie siècle, montrent un ours brun avec un arbousier, une des légendes voulant que le 
                        nom originel de la cité soit Ursaria (le « pays des ours » en latin du fait de leur présence en nombre alors dans les forêts 
                        avoisinantes). Les sept étoiles présentes autour sur la partie bleue, qui représente le ciel symbolisent la Grande Ourse. Enfin, 
                        la couronne, dernier élément qui a été ajouté au blason en 1544, est le symbole de la monarchie.
                        </p>
